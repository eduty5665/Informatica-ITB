CREATE TABLE EMBALAGEM(
	TIPO_EMBAL  INT NOT NULL PRIMARY KEY, 
	DESCRI_EMB VARCHAR(15) NOT NULL,
	SIGLA_EMB VARCHAR(2) NOT NULL)
	GO

CREATE TABLE FORNECEDOR 
(
	COD_FORN  INT NOT NULL PRIMARY KEY,
	NOME_FORN VARCHAR(50) NOT NULL,
	CNPJ_FORN VARCHAR(50) NOT NULL,
	ENDERECO VARCHAR(50) NOT NULL,
	TELEFONE VARCHAR(10) ,
	EMAIL_FORN VARCHAR(50))
	GO
	
CREATE TABLE CLIENTE(
 	COD_CLI  INT NOT NULL PRIMARY KEY,
	CPF_CLI  VARCHAR(11) NOT NULL,
	NOME_CLI VARCHAR(50) NOT NULL ,
	ENDER_CLI VARCHAR(50) NOT NULL ,
	TEL_CLI VARCHAR(10) ,
	EMAIL_CLI VARCHAR(50), 
	SEXO_CLI VARCHAR(1) NOT NULL)
	GO

CREATE TABLE PRODUTO(
	COD_PROD  INT NOT NULL PRIMARY KEY,
	NOME_PROD VARCHAR(50) NOT NULL,
	COD_FORN  INT NOT NULL,
	VALOR_UNIT NUMERIC(10,2) NOT NULL,
	TIPO_EMBAL INT NOT NULL,
	QTDE_EMBALAGEM INT NOT NULL,
	FOREIGN KEY (COD_FORN) REFERENCES FORNECEDOR (COD_FORN),
	FOREIGN KEY (TIPO_EMBAL) REFERENCES EMBALAGEM (TIPO_EMBAL))
	GO

CREATE TABLE PEDIDO (
  	NUM_PED INT NOT NULL,
	COD_CLI  INT NOT NULL,
	COD_PROD  INT NOT NULL,
	QTD_PROD  INT NOT NULL,
	VLOR_UNIT_PROD  NUMERIC(10,2) NOT NULL,
	VLOR_TOT  NUMERIC(10,2) NOT NULL,
	PRIMARY KEY (NUM_PED,COD_PROD),
	FOREIGN KEY (COD_CLI) REFERENCES CLIENTE (COD_CLI),
	FOREIGN KEY (COD_PROD) REFERENCES PRODUTO (COD_PROD))
	GO

